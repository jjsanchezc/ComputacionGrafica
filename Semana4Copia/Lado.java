package Semana4Copia;

public class Lado {
    public int a;
    public int b;

    /**
     * Metodo Constructor para la clase Lado, toma los puntos a tomarse en cuanta para poder hacer el lado
     * @param a primer punto a tomar en cuenta
     * @param b segundo punto a tomar en cuenta
     */
    public Lado(int a, int b){
        this.a = a;
        this.b = b;
    }
}
